void main() {
  // a switch statement
  int day = 5;

  switch (day) {
    case 1:
      print('Monday');
      break;
    case 2:
      print("Tuesday");
      break;
    case 3:
      print("Wednesday");
    case 4:
      print("Thursday");
      break;
    case 5:
      print("Friday");
      break;
    default:
      ('Invalid day');
  }
}
